package _1_Operator_5_relational;

public class Demo2 {
    public static void main(String[] args) {
        System.out.println(10 > 3); //true
        System.out.println(10 < 3); //false
        System.out.println(10 >=3); //true
        System.out.println(10<=3); //false
        System.out.println(10==3); //false
        System.out.println(10!=3); //true

    }
}

