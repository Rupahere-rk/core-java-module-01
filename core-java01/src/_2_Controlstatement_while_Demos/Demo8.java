package _2_Controlstatement_while_Demos;

public class Demo8 {
    public static void main(String[] args) {
        //sum of 1 to n numbers using while loop
        int n = 50;
        int sum =0;
        int i=1;
        while(1<=n){
            sum =sum+i;
            i++;
        }
        System.out.println("sum of n number is =" +sum);
    }
}


